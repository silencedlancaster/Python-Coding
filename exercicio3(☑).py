#pedindo o valor das duas variáveis
numero1 = int(input("Digite o valor do primeiro número: "))
numero2 = int(input("Digite o valor do segundo número: "))

#soma recebe a soma dos números da variável
#produto recebe a multiplicação dos números das variáveis
soma = numero1 + numero2
produto = numero1 * numero2

#o resultado sendo mostrado na tela
print("A soma entre esses números: ", f"{soma:.2f}")
print("O produto entre esses números: ", f"{produto:.2f}")